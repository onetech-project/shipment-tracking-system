#!/usr/bin/env node
const fs = require('fs')
const path = require('path')
const { randomUUID } = require('crypto')

function escapeSqlString(s) {
  return String(s).replace(/'/g, "''")
}

function usage() {
  console.log(
    'Usage: node migrate-sheets-json-to-sql.js --path=path/to/sheets.json [--label="Label"] [--sheet-link="https://..."] [--sheet-id=ID]'
  )
}

const args = process.argv.slice(2)
const opts = {}
for (const a of args) {
  if (a.startsWith('--')) {
    const [k, v] = a.slice(2).split('=')
    opts[k] = v === undefined ? true : v
  }
}

const jsonPath = opts.path || 'apps/backend/src/modules/air-shipments/config/sheets.json'
if (!fs.existsSync(jsonPath)) {
  console.error(`File not found: ${jsonPath}`)
  usage()
  process.exit(1)
}

let arr
try {
  const raw = fs.readFileSync(jsonPath, 'utf8')
  arr = JSON.parse(raw)
  if (!Array.isArray(arr)) throw new Error('Expected array')
} catch (err) {
  console.error('Failed to parse JSON:', err.message)
  process.exit(2)
}

const label = opts.label || `Imported from ${path.basename(jsonPath)}`
const sheetLink = opts['sheet-link'] || ''
const sheetId = opts['sheet-id'] || ''
const syncInterval = opts['sync-interval'] || 15
const enabled = opts.enabled === 'false' ? false : true

const cfgId = randomUUID()
const createdAt = new Date().toISOString()

console.log('-- Migration SQL generated by migrate-sheets-json-to-sql.js')
console.log('-- Review before running. Run with psql or similar.')

console.log(
  `INSERT INTO google_sheet_config (id, sheet_link, sheet_id, sync_interval, enabled, label, created_at, updated_at) VALUES ('${cfgId}', '${escapeSqlString(sheetLink)}', '${escapeSqlString(sheetId)}', ${Number(syncInterval)}, ${enabled ? 'true' : 'false'}, '${escapeSqlString(label)}', now(), now());`
)

for (const item of arr) {
  const id = randomUUID()
  const sheetName = item.sheetName || item.name || ''
  const tableName = item.tableName || ''
  const headerRow = Number(item.headerRow || 1)
  const uniqueKey = JSON.stringify(item.uniqueKey === undefined ? [] : item.uniqueKey)
  const skipNullCols = item.skipNullCols === false ? false : true
  // `table_name` is now a GENERATED column; omit it from INSERT so the DB computes it
  console.log(
    `INSERT INTO google_sheet_sheet_config (id, "googleSheetConfigId", sheet_name, header_row, unique_key, skip_null_cols) VALUES ('${id}', '${cfgId}', '${escapeSqlString(
      sheetName
    )}', ${headerRow}, '${escapeSqlString(uniqueKey)}'::jsonb, ${skipNullCols ? 'true' : 'false'});`
  )
}

console.log(
  '\n-- To apply: psql -d <db> -f <(node scripts/migrate-sheets-json-to-sql.js --path=path/to/sheets.json --label="..." --sheet-link="..." --sheet-id="...")'
)
